# saat inputnya satu, range(X)
# artinya dari 0 hingga X
print(range(10))

# saat inputnya dua, range(X, Y)
# artinya dari X hingga Y
print(range(1, 10))
print(range(3, 10))

# saat inputnya tiga, range(X, Y, Z)
# artinya dari X hingga Y dengan penambahan sebanyak Z
print(range(1, 10, 2))
print (range(1, 10, 3))